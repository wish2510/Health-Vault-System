package vo;

public class ShareRecordVO {
	
	public Long sharerecordid;
	public Long userrecordid;
	public String trustee;
	public String trustedcontact;
	public Long getSharerecordid() {
		return sharerecordid;
	}
	public void setSharerecordid(Long sharerecordid) {
		this.sharerecordid = sharerecordid;
	}
	public Long getUserrecordid() {
		return userrecordid;
	}
	public void setUserrecordid(Long userrecordid) {
		this.userrecordid = userrecordid;
	}
	public String getTrustee() {
		return trustee;
	}
	public void setTrustee(String trustee) {
		this.trustee = trustee;
	}
	public String getTrustedcontact() {
		return trustedcontact;
	}
	public void setTrustedcontact(String trustedcontact) {
		this.trustedcontact = trustedcontact;
	}

	
	
}
